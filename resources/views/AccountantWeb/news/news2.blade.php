<p>
Từ đầu năm, thị trường bất động sản nóng trở lại với sự tái xuất của hàng loạt dự án thuộc nhiều phân khúc từ bình dân tới cao cấp. Đặc biệt, các dự án nhà ở xã hội, chung cư thương mại giá rẻ thu hút số đông khách hàng do bên cạnh khung giá hợp lý, các đối tượng mua nhà còn có cơ hội được hỗ trợ về tài chính từ gói vay lãi suất thấp 30.000 tỷ đồng.
</p>

<p>
Đánh trúng tâm lý muốn mua nhà hoặc chung cư giá rẻ tại thủ đô của người lao động, nhiều môi giới đã nghĩ ra không ít chiêu câu khách. Nếu không cẩn trọng, khách hàng rất dễ bị lừa, chịu tổn thất về tài chính. Một trong những chiêu mới nhất chính là lời quảng cáo sở hữu nhà tại Hà Nội chỉ với vài chục triệu đồng.
</p>

<p>
"Mọi người cẩn trọng nhé. Thực chất 45 triệu đồng chỉ là tiền đặt cọc giữ chỗ ban đầu. Nếu người mua không đảm bảo việc thanh toán tiền nhà đúng đợt hẹn theo điều kiện của chủ đầu tư thì 45 triệu ấy sẽ... đi tong", lời cảnh báo được một nhân viên môi giới bất động sản đưa ra nhưng nhanh chóng bị dìm xuống bởi hàng loạt bình luận mới.
</p>

<p>
Theo tìm hiểu, đây là chiêu quảng cáo liên quan tới một dự án chung cư thương mại giá rẻ đang được quan tâm tại Vân Canh (huyện Hoài Đức, Hà Nội). Giải thích mức giá "45 triệu đồng/căn hộ", nhân viên kinh doanh dự án này cho biết, đây là khoản tiền mà nếu khách hàng bỏ ra, sẽ có ngay hợp đồng mua bán hợp pháp để dùng hợp đồng này vay vốn ngân hàng.
Thực tế, dự án chung cư thương mại giá rẻ nói trên được rao bán ở mức 13-14 triệu đồng/m2. Các căn hộ có diện tích dao động 43-68 m2. Theo thông tin từ chủ đầu tư, dự án này đủ điều kiện áp dụng gói vay 30.000 tỷ. Nhân viên kinh doanh cam kết sẽ hỗ trợ khách hàng vay được gói lãi suất thấp nói trên. Tuy nhiên, người này cũng thừa nhận: "45 triệu đồng chỉ là tiền đặt trước ban đầu để khách giữ chỗ và có hợp đồng mua bán - là điều kiện giúp khách vay vốn ngân hàng".
</p>
