<p class="lead">Để thu hút khách mua chung cư ở một dự án tại xã Vân Canh (Hoài Đức, Hà Nội), môi giới đã tung quảng cáo "nhà bán với giá 45 triệu đồng mỗi căn" gây nhiều tranh cãi.</p>

<p>
Tin quảng cáo gây sốc được đăng tải trên một diễn đàn mua bán ngày 5/5, đã thu hút hàng trăm lượt "like" và bình luận của khách hàng chỉ trong vài tiếng đồng hồ. Nhiều người trong số đó, vì sợ "mất quyền ưu tiên" như cảnh báo của người rao tin, đã ngay lập tức liên hệ với đơn vị bán hàng để hẹn ngày giờ tới xem trực tiếp chân công trình.
</p>

<p>
Thậm chí, tối cùng ngày, như chia sẻ bằng hình ảnh của bên bán, không ít khách hàng đã "nhanh nhạy" tới đặt ngay 45 triệu để sở hữu hợp đồng mua bán căn hộ với quyền chọn vị trí căn đẹp. Một số khách khác tỉnh táo hơn, tỏ ý nghi ngờ và hỏi kỹ về "mức giá" 45 triệu đồng một căn.
</p>

<p>
Bên cạnh làn sóng yêu cầu được cung cấp thông tin ngay lập tức về cơ hội mua nhà giá rẻ, nhiều người có kinh nghiệm đã cảnh báo các thành viên cẩn trọng trước chiêu quảng cáo nói trên. Từ phía nguồn đăng tin, thay vì trả lời và giải thích công khai, bên bán phớt lờ những thông tin chỉ trích, cùng lúc hẹn trao đổi riêng hoặc gọi điện trực tiếp cho những ai có nhu cầu.
</p>

