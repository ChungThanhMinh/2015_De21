<p class="lead">Trong tháng 6 tới, những lứa bò Kobe đầu tiên “made in Việt Nam” xuất chuồng và bán tại thị trường Lâm Đồng.</p>

<p>
Nằm sâu trong bạt ngàn cà phê, trà thuộc thôn 9, xã Tân Lạc, huyện Bảo Lâm (Lâm Đồng) là trang trại bò Kobe duy nhất tại Việt Nam. Trang trại nuôi bò Kobe - một loại bò nổi tiếng thế giới có nguồn gốc từ Nhật Bản với quy trình chăn nuôi nghiêm ngặt khi cho bò uống bia, nghe nhạc, mátxa và thậm chí cấp thẻ “chứng minh” để tiện bề chăm sóc…
</p>

<h4>Cấp thẻ “chứng minh” cho bò</h4>

<p>
Như đã hẹn trước, ông Nguyễn Trí Đức Vũ (39 tuổi) - Giám đốc Công ty Cổ phần bò Kobe Việt Nam, đón chúng tôi và khoác lên người tôi chiếc áo công nhân cùng đôi ủng da cao cổ, hướng tôi ngồi lên hàng ghế sau của chiếc xe điện (loại xe chuyên dụng dùng trong trang trại).
</p>

<hr>
<img class="img-responsive" src="{{asset('images-accountant/news/news3/pic2.jpg')}}" alt="">
<hr>

<p>
Vừa lái ông vừa giới thiệu về trại bò Kobe: “Công ty được thành lập năm 2009 từ sự hợp tác của Việt Nam và Nhật Bản với tỉ lệ vốn là 50-50. Công ty nhập tinh bò Kobe từ Mỹ với giá (40 USD một liều tinh) về phối giống với bò sữa cho ra bò Kobe thế hệ F1.
</p>

<p>
Năm 2011, công ty đưa 120 con bò sữa (nhập 100 con từ Úc, Thái Lan, 20 con ở Việt Nam) về nuôi và cách ly, kiểm dịch sau khi các điều kiện sức khỏe của bò được đảm bảo mới bắt đầu phối tinh. Đến nay, trang trại đã có gần 100 con bò Kobe thế hệ F1 ở độ tuổi (1 tháng đến 26 tháng) từ việc phối giống này. Dự kiến đến tháng 6/2015, trại sẽ xuất chuồng lứa bò đầu tiên”.
</p>

<p>
Là giống thích nghi với xứ lạnh, nên khi nuôi trong môi trường nóng và độ ẩm cao, bò Kobe sẽ yếu hơn các giống bò ở Việt Nam, nhất là dưới 4 tháng tuổi, bò rất dễ mắc bệnh chướng hơi, viêm phổi. Do đó, cần phải có chế độ chăm sóc đặc biệt, quy trình nuôi hết sức nghiêm ngặt. Ngoài lượng thức ăn hàng ngày, bò còn được bổ sung vitamin và khoáng chất thiết yếu.
</p>
