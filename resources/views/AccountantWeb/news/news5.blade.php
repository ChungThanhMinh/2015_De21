<p class="lead">Việc chủ đầu tư tự tăng giá bán nhà đến việc các sàn bất động sản, môi giới tham gia thổi giá, đẩy giá sẽ khiến thị trường gặp nguy hiểm, có thể trở lại cảnh trầm lắng.</p>

<p>
Các cụm từ “bán giá gốc”, “bán suất ngoại giao”… đang xuất hiện ngày càng nhiều trên thị trường nhà đất, thông qua các môi giới nhằm câu khách. Thậm chí, có dự án môi giới còn mập mờ khi có thêm chiêu không tính thuế VAT, phí bảo trì 2%  vào giá căn hộ, để có mức giá rao bán thấp hơn cả giá của chủ đầu tư.
</p>

<p>
Câu chuyện “giá chênh” xuất hiện trên thị trường thời gian gần đây không còn là chuyện lạ, khi người mua nhà nắm được chiến thuật bắt tay giữa chủ đầu tư và các sàn bất động sản (BĐS).
</p>

<p>
Chủ đầu tư thường giao hàng độc quyền cho một vài sàn BĐS, và các đơn vị này với chức năng chính là môi giới sẽ làm thủ thuật, tạo sự khan hiếm ảo, khiến người mua khó tiếp cận. Còn chủ đầu tư, tuy còn “hàng”, nhưng nếu người dân hỏi đến sẽ trả lời là hết. Một mặt là để cùng tạo khan hiếm giả, mặt khác, tiếp tay cho các sàn đẩy giá lên. Điều này khiến cho người mua nhà không biết được đâu là giá thực của căn hộ.
</p>

<p>
Một chiêu thức phổ biến nữa đang được một số chủ dự án BĐS áp dụng, đó là mở bán theo nhiều đợt, với số lượng căn hộ khác nhau và tăng giá cao dần trong các đợt mở bán sau. Cụ thể, lần mở bán đầu tiên bao giờ cũng như lần mở bán “rắc thính” để hút khách hàng, bằng số lượng căn hộ ít ỏi và mức giá khá hấp dẫn, có thể là mức giá rẻ nhất trong các đợt mở bán của chủ đầu tư.
</p>

<p>Vì vậy, theo giới chuyên gia trong ngành, nếu giá BĐS lại bị “đội” lên so với giá thật quá nhiều thì rất nguy hiểm. Không chỉ làm mất lòng tin người tiêu dùng mà có thể làm mất thanh khoản ở một phân khúc, hoặc lại làm trầm lắng cả thị trường như trước đó đã xảy ra.</p>
<p>Các lần mở bán sau sẽ có mức giá tăng dần so với những lần mở bán trước, khiến khách hàng đang “ngắm” dự án, đang có ý định đầu tư sẽ thấy sốt ruột, nghĩ rằng dự án rất hấp dẫn người mua và đã bán hết những căn mở bán trước nên mới tăng giá.</p>

