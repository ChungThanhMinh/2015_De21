<p>
Đà Nẵng là thành phố lớn thứ ba cả nước và là trung tâm thương mại, giáo dục của miền Trung Việt Nam. Thành phố này nổi tiếng với môi trường sạch, bãi biển đẹp và dịch vụ công cộng tốt. Đây cũng được coi là nơi đáng sống nhất Việt Nam.
</p>

<p>
Hai trong số các dự án cơ sở hạ tầng mới quan trọng bậc nhất của Đà Nẵng chính là Khu công nghệ cao Đà Nẵng (Da Nang Hi-Tech Park) và Khu công nghệ thông tin Đà Nẵng (Da Nang IT Park).
</p>

<p>
Khu công nghệ cao Đà Nẵng đang được thi công. Khi hoàn tất, quy mô sẽ đạt 1.129,76 ha. Dự án được kỳ vọng sẽ đẩy mạnh việc phát triển khoa học công nghệ cho thành phố, thông qua thu hút vốn đầu tư địa phương và nước ngoài.
</p>

<p>
Khu công nghệ đang đưa ra những ưu đãi tài chính khá hấp dẫn. Cụ thể, doanh nghiệp sẽ được ưu đãi thuế suất 10% trong 15 năm tính từ khi có doanh thu. Ngoài ra, các doanh nghiệp được miễn thuế 4 năm, giảm 50% trong 9 năm tiếp theo, tính từ năm đầu tiên có thu nhập chịu thuế.
</p>

<p>
Khu công nghệ thông tin Đà Nẵng cũng đưa ra những ưu đãi cho các doanh nghiệp công nghệ cao về thuế thu nhập doanh nghiệp. Cụ thể, thuế suất áp dụng trong vòng 4 năm đầu tiên kể từ khi thu lợi nhuận là 0%. Trong 9 năm kế tiếp, mức này là 5% và 2 năm tiếp theo là 10%. Tiền thuê đất cũng được miễn phí lên tới 50 năm cho những "người thuê đáng tin cậy", tùy thuộc vào loại hình công nghiệp và quy mô đầu tư.
</p>
